# with open('mrna_test.txt', 'r') as infile:
with open('rosalind_mrna.txt', 'r') as infile:
    protein = infile.readline().strip()

protein = list(protein)
protein.append(None) # add stop codon

lst = []
for aa in protein:
    codons = []
    for k, v in rna_codon_dic.items():
        if v == aa:
            codons.append(k)
    lst.append(codons)

lst = [len(sublst) for sublst in lst]
ans = 1
for i in lst:
    ans *= i

ans = ans % 1000000

print(ans)

pyperclip.copy(ans)
